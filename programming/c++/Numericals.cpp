#include <iostream>

int main()
{
    std::cout << 420 + 69 << std::endl;
    std::cout << 420 - 69 << std::endl;
    std::cout << 420 * 69 << std::endl;

    //return int
    std::cout << 420 / 69 << std::endl;

    //return float
    std::cout << 420.0f / 69.0f << std::endl;
    std::cout << 420 + 69 << std::endl;

    return 0;
}