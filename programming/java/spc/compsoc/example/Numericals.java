package spc.compsoc.example;
public class Numericals{
  public static void main(String[] args){
    System.out.println(420 + 69);
    System.out.println(420 - 69);
    System.out.println(420 * 69);
    //Integer division (=6);
    System.out.println(420 / 69);
    //Decimal Division (=6.08695652174...);
    System.out.println((double) 420 / 69);
    System.out.println(420 % 69);
    }
  }
